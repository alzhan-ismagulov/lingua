  <h2>Консульская легализация</h2>
  <a href="http://online.prg.kz/doc/lawyer/?doc_id=30073360&amp;sub=SUB0#SUB0">Приказом</a><strong>&nbsp;министра иностранных дел Республики Казахстан от 7 сентября 2006 года № 08-1/262</strong>&nbsp;(зарегистрирован в Реестре государственной регистрации нормативных правовых актов Республики Казахстан 12 октября 2006 года под № 4418) внесены изменения и дополнения в&nbsp;<a href="http://online.prg.kz/doc/lawyer/?doc_id=1021284&amp;sub=SUB0#SUB0">приказ</a>&nbsp;министра иностранных дел Республики Казахстан от 21 ноября 2000 года № 264 &laquo;Об утверждении&nbsp;<strong>Правил консульской легализации</strong>&nbsp;(далее &mdash; Правила, в соответствующих падежах)&raquo;. В рамках настоящего обзора мы рассмотрим Правила в новой редакции, которая будет действовать со дня официального опубликования.<br />
  <br />
В соответствии с Правилами&nbsp;<strong>консульская легализация</strong>&nbsp;(далее &mdash; легализация) заключается в проверке соответствия документов действующему законодательству государства пребывания и представляет собой установление и засвидетельствование подлинности подписи должностного лица и печати на таких документах с целью использования их в другом государстве. Легализация&nbsp;<strong>подтверждает правомочность</strong>&nbsp;документов и актов в международном общении. За содержание документа консул ответственности не несет.&nbsp;<br />
<br />
<strong>Целью легализации</strong>&nbsp;является обеспечение гарантии органам страны, где документ предназначен действовать, в том, что документ является подлинным и оформлен должным образом.&nbsp;<br />
<br />
Функции по легализации выполняют:&nbsp;<strong>за рубежом</strong>&nbsp;&mdash; загранучреждения Республики Казахстан, а&nbsp;<strong>в Республике Казахстан</strong>&nbsp;&mdash; Департамент консульской службы Министерства иностранных дел Республики Казахстан (далее &mdash; Департамент консульской службы).&nbsp;<br />
<br />
В загранучреждениях Республики Казахстан за рубежом функции по легализации документов и актов возлагаются на консулов Республики Казахстан. По поручению консула легализация может осуществляться и другими консульскими должностными лицами.<br />
<br />
Консул, согласно Правилам,&nbsp;<strong>может принимать к легализации документы и акты</strong>, составленные с участием властей консульского округа, удостоверенные ими или исходящие от этих властей. Государственные органы Республики Казахстан могут принимать к рассмотрению документы и акты, исходящие от организаций и учреждений иностранного государства лишь при наличии легализации, если иное не предусмотрено законодательством Республики Казахстан или международным договором, участниками которого являются Республика Казахстан и государство, от организации и учреждений которого исходят документы и акты.&nbsp;<br />
<br />
В соответствии с Правилами&nbsp;<strong>легализация доверенностей</strong>&nbsp;на совершение всякого рода действий за рубежом от имени организаций и учреждений Республики Казахстан, уполномоченных совершать такие операции, а также других документов, связанных с деятельностью указанных организаций и учреждений, проводится только Департаментом консульской службы. Легализации&nbsp;<strong>не подлежат</strong>&nbsp;документы и акты, которые противоречат законодательству Республики Казахстан, или могут по своему содержанию нанести вред интересам Республики Казахстан, или содержат сведения, порочащие честь и достоинство граждан Республики Казахстан. Последними изменениями также уточнено, что легализации не подлежат документы и акты в случаях, предусмотренных международными договорами Республики Казахстан и законодательными актами Республики Казахстан.<br />
<br />
Для осуществления легализации документов и актов&nbsp;<strong>загранучреждениями Республики Казахстан за рубежом</strong>&nbsp;консул должен иметь образцы подписей и печатей должностных лиц властей консульского округа, уполномоченных удостоверять документы и акты, исходящие от организаций и учреждений государства пребывания. В случаях, когда у консула возникает сомнение в том, действительно ли представленные к легализации документы и акты соответствуют законодательству государства пребывания, консул обращается за официальным разъяснением к властям консульского округа.&nbsp;<br />
<br />
В случаях, когда&nbsp;<strong>в порядке истребования документов из Республики Казахстан</strong>&nbsp;консулу поступили документы и акты, составленные в Республике Казахстан, но не легализованные в Департаменте консульской службы, и если по законодательству государства пребывания требуется легализация таких документов и актов, консул легализует их и передает заинтересованным лицам, организациям и учреждениям. В случае поступления от отдельных граждан, организаций и учреждений на легализацию документов и актов, составленных на территории Республики Казахстан, но не легализованных в Департаменте консульской службы, консул должен направить такие документы и акты в Департамент консульской службы для определения их подлинности. При получении положительного ответа консул легализует такие документы и акты в общем порядке. В необходимых случаях консул может легализовать документы и акты, предназначенные для действия на территории третьего государства.&nbsp;<br />
<br />
Согласно Правилам, Департамент консульской службы&nbsp;<strong>легализует в Республике Казахстан</strong>&nbsp;документы и акты, исходящие от иностранных организаций и учреждений, если они предварительно легализованы в загранучреждении иностранного государства происхождения документов. В случаях, когда документ или акт, исходящий от организаций и учреждений иностранного государства, не может быть легализован в порядке, установленном Правилами, Департамент консульской службы вправе совершать на документе или акте особую отметку о том, что не встречается возражений к принятию официальными учреждениями Республики Казахстан к своему рассмотрению настоящего документа. В этом случае признание юридической силы за такими документами и актами по совокупности обстоятельств дела лежит всецело на органах, их принимающих.&nbsp;<br />
<br />
Департамент консульской службы легализует также документы и акты, исходящие от организаций и учреждений Республики Казахстан и&nbsp;<strong>предназначенные для предъявления за рубежом</strong>.&nbsp;<br />
<br />
В соответствии с Правилами документы принимаются на легализацию&nbsp;<strong>как в подлинниках, так и в нотариально заверенных копиях</strong>. В нотариально заверенных копиях легализуются учредительные документы юридических лиц при условии, что данные документы зарегистрированы в уполномоченном государственном органе Республики Казахстан и на них имеются соответствующие отметки и печати о регистрации, а также представлено свидетельство об их государственной регистрации. Если легализация в иностранном дипломатическом представительстве или загранучреждении обусловлена представлением перевода документа с государственного или русского языка на иностранный язык, то легализация нотариально заверенного документа в Департаменте консульской службы может совершаться вместе с его переводом. Перевод может располагаться колонкой на странице с правой стороны (текст на государственном или русском языке слева) или же на отдельной странице. В последнем случае перевод скрепляется с документом. Последней редакцией Правил специально оговорено, что нотариальное засвидетельствование перевода документа или акта консулом&nbsp;<strong>не приравнивается к его легализации</strong>.<br />
<br />
Документы и акты, представляемые для легализации, должны быть написаны ясно и четко, подписи должностных лиц и оттиски печати должны быть отчетливыми. Пробелы на документах и актах должны быть прочеркнуты. Поправки и приписки должны быть оговорены перед подписью лиц, подписавших документ, и повторены в легализационной надписи, исполненной на государственном или русском языке либо, при необходимости, на иностранном языке. Эти приписки и поправки должны быть сделаны так, чтобы все ошибочно написанное, а затем исправленное и зачеркнутое можно было прочесть в первоначальном виде. Если поправки и приписки сделаны в легализационной надписи, то они оговариваются и подписываются только должностными лицами, совершающими легализацию.&nbsp;<br />
<br />
В тех случаях, когда для легализационной надписи нет места, она совершается на отдельном листе, который подшивается к документу или акту и скрепляется с последним печатью и подписью лица, совершившего легализацию. После того как должностное лицо убедилось, что в соответствии с требованиями Правил документ или акт может быть легализован, на последнем делается&nbsp;<strong>надпись установленного образца</strong>.&nbsp;<br />
<br />
За совершение в соответствии с Правилами действия по легализации документов и актов с заинтересованных физических и юридических лиц взимается консульский сбор в соответствии с&nbsp;<a href="http://online.prg.kz/doc/lawyer/?doc_id=1023502&amp;sub=SUB5090000#SUB5090000">Кодексом</a>&nbsp;Республики Казахстан &laquo;О налогах и других обязательных платежах в бюджет&raquo; (Налоговый кодекс) и подлежат возмещению фактические расходы, связанные с совершением этого действия.
