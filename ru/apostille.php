  <p><strong>Что такое апостиль? Где и как его получить?</strong><br />
    В соответствии с Гаагской конвенцией на документах, совершенных   компетентными  органами одного государства, и предназначенных для   использования на территории  другого государства, проставляется&nbsp;<strong>специальный штамп (апостиль)</strong>.    Согласно статье 5 Конвенции апостиль удостоверяет подлинность подписи,    качество, в котором выступало лицо, подписавшее документ, и в   надлежащем  случае, подлинность печати или штампа, которым скреплен этот   документ.<br />
    Подпись, печать или штамп, проставляемые компетентным органом на   апостиле, не  требуют никакого дальнейшего заверения или легализации, а   документ, на котором  проставлен апостиль, может быть использован в   любой из стран - участниц  Гаагской Конвенции.&nbsp;<br />
  <br />
    В соответствии с Постановлением Правительства Республики Казахстан   № 545 от 24  апреля 2001 года в качестве организаций, обладающих   полномочиями по  проставлению апостиля определены следующие   государственные органы:<br />
  <br />
  &bull; Министерство юстиции Республики Казахстан&nbsp;<br />
  &bull; Министерство образования и науки Республики Казахстан&nbsp;<br />
  &bull; Министерство внутренних дел Республики Казахстан&nbsp;<br />
  &bull; Комитет информации и архивов Министерства культуры и информации Республики  Казахстан<br />
  &bull; Комитет по судебному администрированию при Верховном Суде Республики  Казахстан&nbsp;<br />
  &bull; Министерство финансов Республики Казахстан&nbsp;<br />
  &bull; Генеральная Прокуратура Республики Казахстан&nbsp;<br />
  &bull; Министерство обороны Республики Казахстан&nbsp;<br />
  &bull; Комитет национальной безопасности Республики Казахстан<br />
  </p>
  <p> <strong>Как узнать: нужна консульская легализация или апостиль?</strong><br />
    Если страна, в которой предполагается использовать документ,   является  участницей Гаагской конвенции 1961 года - значит нужен штамп    &quot;апостиль&quot;. В противном случае - консульская легализация.<br />
    В любом случае самым важным источником достоверной информации   является  организация, в которую должен представляться легализованный   документ, т.к.  иногда даже в пределах одного государства разные органы   предъявляют разные  требования к легализации иностранных документов.   Поэтому вам необходимоуточнять  требования к виду легализации в органе, в который собираетесь подавать  легализованный документ.<br />
  </p>
  <p> <strong>Что такое апостиль?</strong>&nbsp;<br />
    Проставление штампа &quot;апостиль&quot; (иногда данную процедуру также    называют &quot;упрощенная легализация&quot; или &quot;апостилирование&quot;)  применяется   для отправки документа в страны, присоединившиеся к Гаагской  конвенции   от 5 октября 1961 года, отменяющей требование консульской легализации  и   вводящей процедуру упрощенной легализации - проставление штампа    &quot;апостиль&quot;.&nbsp;<br />
  <br />
    Упрощенной такая процедура называется именно потому, что, с одной   стороны,  апостилирование осуществляется достаточно быстро и только   одним уполномоченным  органом, а, с другой стороны, документ приобретает   юридическую силу на  территории всех стран, присоединившихся к Гаагской   конвенции.&nbsp;<br />
  <br />
    Если же страна, в которую вы готовите документ, не является   участницей Гаагской  конвенции, необходима консульская легализация. Это   более сложная процедура,  включающая в себя удостоверение документа в   органах Министерства юстиции РК,  органах МИД РК, а затем в консульстве   страны назначения в Республике Казахстан.  При этом документ будет иметь   юридическую силу на территории только той страны,  отметка консульства   которой на нем стоит.<br />
  <br />
    Также выбор процедуры легализации может быть основан на типе   документа, так как  существует ряд документов, которые не подлежат ни   консульской легализации, ни  апостилированию. В основном это касается   коммерческих документов: договоров,  инвойсов, коносаментов, различных   товарораспорядительных документов и других  документов, связанных с   внешнеэкономической деятельностью, и для них существует  отдельная   процедура - легализация документа в Торгово-промышленной палате РК (с    недавних пор входит в состав Национальной экономической палаты   Казахстана  &quot;Атамекен&quot;), а затем в консульстве страны назначения.&nbsp;<br />
    Согласно Гаагской конвенции 1961 г., апостиль имеет форму квадрата   со стороной  не менее 9 см и должен соответствовать образцу,   приложенному к Конвенции.<br />
  </p>
  <p> <strong>Текст апостиля должен содержать следующие реквизиты:</strong></p>
  <ul type="disc">
    <li>Наименование государства,       выдавшего апостиль</li>
    <li>Фамилия лица, подписавшего       удостоверяемый апостилем документ</li>
    <li>Должность лица, подписавшего       удостоверяемый апостилем документ</li>
    <li>Наименование учреждения,       печатью/штампом которого скреплён документ, удостоверяемый апостилем</li>
    <li>Название города, в котором       проставлен апостиль</li>
    <li>Дата проставления апостиля</li>
    <li>Название органа, проставившего       апостиль</li>
    <li>Номер апостиля</li>
    <li>Печать/штамп учреждения,       проставившего апостиль</li>
    <li>Подпись должностного лица,       проставившего апостиль</li>
  </ul>
  <p>Апостиль может быть составлен как на одном из официальных языков   Конвенции  (французский либо английский), так и на национальном языке   государства,  проставившего апостиль. На практике надписи на апостиле   часто дублируются на  двух языках (одном из языков Конвенции и   национальном).<br />
    <br />
    Заголовок &laquo;Apostille (Convention de la Haye du 5 octobre 1961)&raquo; должен быть дан  на французском языке.<br />
  <br />
    Согласно Конвенции, апостиль проставляется на самом документе или на отдельном  листе, скрепляемом с документом.<br />
  <br />
    На практике в различных государствах используются различные   способы  проставления апостилей: резиновый штамп, клей, (разноцветные)   ленты, сургучная  печать, рельефная печать, самоклеящиеся стикеры и т.   д., а также в качестве  приложения к документу, что осуществляется при   помощи клея, крепёжных колец,  скрепок и т. д.<br />
  </p>
