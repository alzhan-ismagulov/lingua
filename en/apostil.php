      <p><strong>What  is an apostille? Where and how to get it?</strong><br />
        In accordance with Hague Convention on the documents  executed by the competent authorities of one state and intended for use on the  territory of another state, the <strong>special stamp (apostille)</strong> is used.  According to Article 5 of the Convention the apostille authenticates the  signature, role of person signing the document, and where appropriate, the  identity of the seal or stamp which the document contains.<br />
        The signature, seal and stamp which is provided on apostille  by the competent authority, require no further certification or legalization,  and the document with apostille can be used in any country - member of the  Hague Convention.<br />
        In  accordance with Resolution of the Government of the Republic of Kazakhstan No.  545 of 24 April 2001 the organizations with the authority to stamp apostille  are as follows:</p>
      <p>&bull;  The Ministry of Justice of the Republic of Kazakhstan<br />
  &bull;  Ministry of Education and Science of the Republic of Kazakhstan<br />
  &bull;  The Ministry of Interior Affairs of the Republic of Kazakhstan<br />
  &bull;  The Committee of Information and Archives of the Ministry of Culture and  Information of the Republic of Kazakhstan<br />
  &bull;  The Committee on Court Administration of the Supreme Court of the Republic of  Kazakhstan<br />
  &bull;  The Ministry of Finance of the Republic of Kazakhstan<br />
  &bull;  The General Prosecutor's Office of the Republic of Kazakhstan<br />
  &bull;  The Ministry of Defense of the Republic of Kazakhstan<br />
  &bull;  The National Security Committee of the Republic of Kazakhstan<br />
  <strong>How will you know when need consular legalization or  apostille?</strong><br />
        If  the country in which the document will be used is a party of Hague Convention as  of 1961 - so you need a stamp &quot;Apostille&quot;. Otherwise - consular  legalization.<br />
        In  any case, the most important source of reliable information is the organization  to which legalized document will be submitted, as sometimes even within one state  different bodies have different requirements for legalization of foreign  documents. So you need to specify the requirements for legalization in the body  to which you are going to apply legalized document.<br />
  <br />
  <strong>What is an apostille?</strong><br />
        Stamping  &quot;apostille&quot; (sometimes this procedure is also called &quot;simplified  legalization&quot; or &quot;Apostille&quot;) is used to send a document to the  countries that joined Hague Convention of 5 October 1961 abolishing the requirement  of consular legalization and adopting simplified procedure of legalization -  stamping &quot;apostille&quot;.<br />
        This  procedure is called simplified because, on the one hand, the apostille is very fast  and provided by only one authorized body and, on the other hand, the document  becomes valid in all countries that have joined to Hague Convention.<br />
        If  country for which you are preparing document is not a member of Hague Convention,  consular legalization is required. This is more complicated procedure,  including the identification of document in the Ministry of Justice of the  Republic of Kazakhstan, bodies of Ministry of Foreign Affairs of the Republic  of Kazakhstan and then in consulate of destination country in the Republic of  Kazakhstan. In this case, the document will be valid only in the territory of  country which consulate mark is stamped in document.<br />
  <br />
        Also the selection of the legalization procedure may be based on document type,  as there are a number of documents which are not the subject of any consular  legalization or apostille. Mainly this applies to commercial documents:  contracts, invoices, bills of lading and various documents of title and other  documents related to foreign trade activities, and for them there is a separate  procedure &ndash; the legalization of document in Chamber of Commerce of the Republic  of Kazakhstan (recently it is a part of &quot;Atameken&quot; - National Economic  Chamber of Kazakhstan), and then in consulate of destination country.<br />
        According  to Hague Convention1961 the Apostille has a form of square with a side of not  less than 9cm and should matched the model attached to Convention.<br />
  <strong>The  text of apostille should contain the following details:</strong><br />
  &bull;  Name of country issuing an apostille<br />
  &bull;  Name of person signing the document certified by apostille<br />
  &bull;  Position of person signing the document certified by apostille<br />
  &bull;  Name of institution which seal/stamp is on the document certified by apostille<br />
  &bull;  Name of city where the apostille was made<br />
  &bull;  Date of apostilling<br />
  &bull;  Name of body providing apostille<br />
  &bull;  Number of apostille<br />
  &bull;  Seal/stamp of body providing the apostille<br />
  &bull;  Signature of officer stamping apostille<br />
        Apostille  can be made up as in one of the official languages ??of the Convention (French  or English) or in national language of the country providing the apostille. In  practice the inscription on apostille are often duplicated in two languages  ??(one of the Convention languages ?? and one national).<br />
        Title  &laquo;Apostille (Convention de la Haye du 5 octobre 1961)&quot; should be in French.<br />
        According  to the Convention the apostille should be placed on the document or on a  separate sheet attached to the document.<br />
        In  practice different countries use different methods of the apostilling such as:  rubber stamp, glue, (colored) ribbons, wax seal, relief printing, self-adhesive  stickers, etc., as well as an appendix to the document, which is carried out by  means of glue, retaining rings, clips, etc.</p>
