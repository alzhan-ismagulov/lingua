      <p><strong>Консулдық заңдастыру немесе апостиль қажет па  екенін қалай білуге болады?</strong><br />
        Егер құжат қолданылатын ел 1961 жылғы Гаага конвенциясының қатысушысы болып  табылса –&quot;апостиль&quot; мөртабаны қажет. Немесе – консулдық заңдастыру  қажет.<br />
        Шынайы ақпарат көзі болып заңдастырылған құжат ұсынылатын  ұйым табылады, себебі бір мемлекет шеңберінде түрлі органдар шетел құжаттарын  заңдастыруға түрлі талаптар қояды. Сондықтан заңдастырылған құжат тапсырылатын  органда заңдастыру түріне қойылатын талаптарды нақтылау қажет.<br />
  <strong>Апостиль дегеніміз не?</strong> <br />
  &quot;Апостиль&quot; мөртабанын қою (кейде  бұл рәсімді «жеңілдетілген заңдастыру» немесе «апостильдеу» деп аталады) 1961  жылғы 5 қазандағы Гаага конвенциясына қосылған, консулдық заңдастыру  талаптарының күшін жоятын және жеңілдетілген заңдастыру рәсімін енгізетін елдерге  құжаттар жолдау кезінде - &quot;апостиль&quot; мөртабанын қою қолданылады.<br />
        Бір жағынан апостильдеу мейлінше жылдам және оны тек қана бір өкілетті  орган қоюына, ал екінші жағынан құжаттың Гаага конвенциясына қосылған барлық  елдердің аумағында заңды күшіне ие болуына байланысты жеңілдетілген деп  аталады.<br />
        Егер сіз құжатты дайындаған ел Гаага конвенциясына қатыспаса, оны консулдық  заңдастыру қаже болады. Бұл айтарлықтай күрделі рәсім, ол құжатты ҚР Әділет  министрлігінде, ҚР СІМ органдарында, бұдан кейін Қазақстан Республикасында  баратын елдің консулдылығында куәландыруды талап етеді. Бұл жерде құжат  консулдық белгілісі бар елдің аумағында ғана заңды күшіне ие болады.<br />
        Сондай-ақ заңдастыру рәсімі құжат типіне  негізделуі мүмкін, себебі консулдық заңдастыруға да, сондай-ақ апостильдеуге де  жатпайтын құжаттар бар. Бұл негізінен коммерциялық құжаттарға қатысты: шарттар,  инвойстар, коносаменттер, түрлі тауарды таратушы құжаттар және сыртқы  экономикалық қызметке байланысты өзге құжаттар, олар үшін жекелеген рәсім бар –  құжат ҚР Сауда-өнеркәсіп палатасында (жуырда Қазақстанның «Атамекен» Ұлттық  экономикалық палатасының құрамына кірді), бұдан кейін баратын елдің  консулдығында заңдастырылады.<br />
