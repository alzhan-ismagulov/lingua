        Гаага конвенциясына сәйкес,  басқа бір мемлекеттің аумағында пайдалануға арналған, белгілі бір мемлекеттің  өкілетті органдар берген құжаттарда&nbsp;<strong>арнайы мөртабан қойылады (апостиль) қойылады</strong>. Конвенцияның 5-бабына сәйкес, апостиль  қолдардың түпнұсқалылығын, құжатқа қол қойған адамның кім екендігін және тиісті  жағдайларда бұл құжат бекітілген мөрдің немесе мөртаңбаның түпнұсқалылығын  куәландырады. <br />
